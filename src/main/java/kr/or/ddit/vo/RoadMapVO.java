package kr.or.ddit.vo;

public class RoadMapVO {

	private int projId;
	private String rmSe;
	private String rmCn;
	public int getProjId() {
		return projId;
	}
	public void setProjId(int projId) {
		this.projId = projId;
	}
	public String getRmSe() {
		return rmSe;
	}
	public void setRmSe(String rmSe) {
		this.rmSe = rmSe;
	}
	public String getRmCn() {
		return rmCn;
	}
	public void setRmCn(String rmCn) {
		this.rmCn = rmCn;
	}
}
